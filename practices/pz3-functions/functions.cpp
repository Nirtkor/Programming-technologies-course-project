// Практическое занятие №3. Вариант В. Индивидуальный вариант №1.
// Тема: взаимодействие функций в программах.
//   Задание 1 — слагаемые типов int и double, тип суммы long int,
//               вывести размер памяти суммы;
//   Задание 2 — массив float из 5 элементов, вычисляемый показатель —
//               наименьшая дробная часть элемента.
// Программа выполнена в двух частях:
//   часть 1 — взаимодействие функций БЕЗ глобальных переменных
//             (данные через параметры, результат через return);
//   часть 2 — взаимодействие функций ЧЕРЕЗ глобальные переменные.

#include <iostream>
#include <clocale>
#include <cmath>
using namespace std;

// ===== ЧАСТЬ 1. Без глобальных переменных =====

// Задание 1: возвращает сумму двух чисел (тип суммы — long int)
long int summaLocal(int a, double b)
{
    long int s = a + b;   // приведение результата к long int
    return s;
}

// Задание 2: возвращает наименьшую дробную часть элементов массива
float minFracLocal(float m[5])
{
    float minFrac = m[0] - floor(m[0]);   // дробь 1-го элемента
    for (int i = 1; i < 5; i++)
    {
        float frac = m[i] - floor(m[i]);  // дробь i-го элемента
        if (frac < minFrac)
            minFrac = frac;
    }
    return minFrac;
}

// ===== ЧАСТЬ 2. Через глобальные переменные =====

int      g_a;        // первое слагаемое (задание 1)
double   g_b;        // второе слагаемое (задание 1)
long int g_summa;    // сумма — результат задания 1
float    g_mass[5];  // массив (задание 2)
float    g_minFrac;  // наименьшая дробь — результат задания 2

// Задание 1: данные из глобальных переменных, результат — в глобальную
void summaGlobal()
{
    g_summa = g_a + g_b;
}

// Задание 2: обработка глобального массива, результат — в глобальную
void minFracGlobal()
{
    g_minFrac = g_mass[0] - floor(g_mass[0]);
    for (int i = 1; i < 5; i++)
    {
        float frac = g_mass[i] - floor(g_mass[i]);
        if (frac < g_minFrac)
            g_minFrac = frac;
    }
}

int main()
{
    setlocale(LC_ALL, "Russian");   // вывод русских букв

    // ----- ЧАСТЬ 1: локальные переменные -----
    cout << "=== Часть 1. Без глобальных переменных ===" << endl;

    int a = 7;            // данные задания 1 задаются в main
    double b = 3.5;
    long int s = summaLocal(a, b);     // результат через return
    cout << "Задание 1. Сумма = " << s << endl;
    cout << "Размер памяти суммы = " << sizeof(s) << " байт" << endl;

    float m[5];           // массив задания 2
    cout << "Задание 2. Введите 5 дробных чисел: ";
    for (int i = 0; i < 5; i++)
        cin >> m[i];
    float f = minFracLocal(m);         // результат через return
    cout << "Наименьшая дробная часть = " << f << endl;

    // ----- ЧАСТЬ 2: глобальные переменные -----
    cout << "\n=== Часть 2. Через глобальные переменные ===" << endl;

    g_a = 7;              // данные кладём в глобальные переменные
    g_b = 3.5;
    summaGlobal();        // функция сама берёт данные и пишет результат
    cout << "Задание 1. Сумма = " << g_summa << endl;
    cout << "Размер памяти суммы = " << sizeof(g_summa) << " байт" << endl;

    cout << "Задание 2. Введите 5 дробных чисел: ";
    for (int i = 0; i < 5; i++)
        cin >> g_mass[i];
    minFracGlobal();
    cout << "Наименьшая дробная часть = " << g_minFrac << endl;

    return 0;
}
