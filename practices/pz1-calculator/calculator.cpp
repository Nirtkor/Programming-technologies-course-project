// Практическое занятие №1. Вариант В.
// Программа "Калькулятор": последовательно выполняет арифметические
// действия, сохраняя результат от операции к операции. Работа
// завершается при вводе символа 'z' с выводом последнего результата.

#include <iostream>
#include <clocale>
using namespace std;

int main()
{
    setlocale(LC_ALL, "Russian");   // вывод русских букв в консоль

    double result;   // накопленный результат вычислений
    char   op;       // знак арифметической операции
    double num;      // очередное вводимое число

    // Ввод первого числа
    cout << "Введите первое число: ";
    cin >> result;

    // Основной цикл вычислений
    while (true)
    {
        cout << "Введите знак операции (+ - * /) или z для выхода: ";
        cin >> op;

        // Символ 'z' завершает работу программы
        if (op == 'z')
            break;

        cout << "Введите следующее число: ";
        cin >> num;

        // Выбор действия по знаку операции
        switch (op)
        {
            case '+': result = result + num; break;
            case '-': result = result - num; break;
            case '*': result = result * num; break;
            case '/':
                if (num != 0)
                    result = result / num;
                else
                    cout << "Ошибка: деление на ноль!" << endl;
                break;
            default:
                cout << "Неизвестная операция!" << endl;
        }

        // Промежуточный результат после каждой операции
        cout << "Текущий результат: " << result << endl;
    }

    // Вывод последнего полученного результата
    cout << "Итоговый результат: " << result << endl;
    return 0;
}
